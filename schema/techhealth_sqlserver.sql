  CREATE TABLE [dbo].[Customers] (
    [user_id]           VARCHAR (10)  NOT NULL,
    [age]               INT           NOT NULL,
    [gender]            CHAR (1)      NOT NULL,
    [city]              VARCHAR (100) NOT NULL,
    [state]             VARCHAR (50)  NULL,
    [country]           VARCHAR (50)  NOT NULL,
    [occupation]        VARCHAR (100) NULL,
    [income_bracket]    VARCHAR (20)  NULL,
    [registration_date] DATE          DEFAULT (getdate()) NOT NULL,
    [subscription_type] VARCHAR (50)  NOT NULL,
    PRIMARY KEY CLUSTERED ([user_id] ASC),
    CHECK ([age]>=(0) AND [age]<=(120)),
    CHECK ([gender]='F' OR [gender]='M'),
    CHECK ([subscription_type]='Enterprise' OR [subscription_type]='Premium' OR [subscription_type]='Basic')
);


CREATE TABLE [dbo].[Devices] (
    [device_id]                     VARCHAR (10)   NOT NULL,
    [user_id]                       VARCHAR (10)   NOT NULL,
    [device_type]                   VARCHAR (100)  NOT NULL,
    [purchase_date]                 DATE           DEFAULT (getdate()) NOT NULL,
    [last_sync_date]                DATE           NOT NULL,
    [firmware_version]              VARCHAR (10)   NOT NULL,
    [battery_life_days]             DECIMAL (3, 1) NOT NULL,
    [sync_frequency_daily]          INT            NOT NULL,
    [active_hours_daily]            DECIMAL (3, 1) NOT NULL,
    [total_steps_recorded]          BIGINT         NOT NULL,
    [total_workouts_recorded]       INT            NOT NULL,
    [sleep_tracking_enabled]        BIT            DEFAULT ((0)) NOT NULL,
    [heart_rate_monitoring_enabled] BIT            DEFAULT ((0)) NOT NULL,
    [gps_enabled]                   BIT            DEFAULT ((0)) NOT NULL,
    [notification_enabled]          BIT            DEFAULT ((0)) NOT NULL,
    [device_status]                 VARCHAR (50)   NOT NULL,
    PRIMARY KEY CLUSTERED ([device_id] ASC),
    CHECK ([active_hours_daily]>=(0) AND [active_hours_daily]<=(24)),
    CHECK ([battery_life_days]>=(0)),
    CHECK ([device_status]='Retired' OR [device_status]='Inactive' OR [device_status]='Active'),
    CHECK ([sync_frequency_daily]>=(0)),
    CHECK ([total_steps_recorded]>=(0)),
    CHECK ([total_workouts_recorded]>=(0)),
    CONSTRAINT [FK_Devices_Customers] FOREIGN KEY ([user_id]) REFERENCES [dbo].[Customers] ([user_id]) ON DELETE CASCADE ON UPDATE CASCADE
);


CREATE TABLE [dbo].[Sales] (
    [sale_id]           VARCHAR (10)    NOT NULL,
    [user_id]           VARCHAR (10)    NOT NULL,
    [sale_date]         DATE            DEFAULT (getdate()) NOT NULL,
    [product_id]        VARCHAR (20)    NOT NULL,
    [product_name]      VARCHAR (100)   NOT NULL,
    [product_category]  VARCHAR (50)    NOT NULL,
    [unit_price]        DECIMAL (10, 2) NOT NULL,
    [quantity]          INT             NOT NULL,
    [discount_applied]  DECIMAL (5, 2)  NULL,
    [total_amount]      DECIMAL (10, 2) NOT NULL,
    [payment_method]    VARCHAR (50)    NOT NULL,
    [subscription_plan] VARCHAR (50)    NULL,
    [sales_channel]     VARCHAR (50)    NOT NULL,
    [region]            VARCHAR (50)    NOT NULL,
    [sales_rep_id]      VARCHAR (10)    NOT NULL,
    PRIMARY KEY CLUSTERED ([sale_id] ASC),
    CHECK (discount_applied IS NULL OR (discount_applied >= 0 AND discount_applied <= 100)),
    CHECK ([payment_method]='Bank Transfer' OR [payment_method]='PayPal' OR [payment_method]='Credit Card'),
    CHECK ([product_category]='Subscription' OR [product_category]='Accessory' OR [product_category]='Device'),
    CHECK ([quantity]>(0)),
    CHECK ([sales_channel]='Direct Sales' OR [sales_channel]='Retail' OR [sales_channel]='Online'),
    CHECK ([total_amount]>=(0)),
    CHECK ([unit_price]>=(0)),
    CONSTRAINT [FK_Sales_Customers] FOREIGN KEY ([user_id]) REFERENCES [dbo].[Customers] ([user_id]) ON DELETE CASCADE ON UPDATE CASCADE
);


CREATE TABLE [dbo].[HealthMetrics] (
    [record_id]              VARCHAR (10)   NOT NULL,
    [user_id]                VARCHAR (10)   NOT NULL,
    [month_date]             DATE           NOT NULL,
    [avg_heart_rate]         INT            NOT NULL,
    [avg_resting_heart_rate] INT            NOT NULL,
    [avg_daily_steps]        INT            NOT NULL,
    [avg_sleep_hours]        DECIMAL (3, 1) NOT NULL,
    [avg_deep_sleep_hours]   DECIMAL (3, 1) NOT NULL,
    [avg_daily_calories]     INT            NOT NULL,
    [avg_exercise_minutes]   INT            NOT NULL,
    [avg_stress_level]       DECIMAL (3, 1) NULL,
    [avg_blood_oxygen]       DECIMAL (4, 1) NOT NULL,
    [total_active_days]      INT            NOT NULL,
    [workout_frequency]      INT            NOT NULL,
    [achievement_rate]       DECIMAL (3, 2) NULL,
    PRIMARY KEY CLUSTERED ([record_id] ASC),
    CHECK ([achievement_rate]>=(0) AND [achievement_rate]<=(1)),
    CHECK ([avg_blood_oxygen]>=(80) AND [avg_blood_oxygen]<=(100)),
    CHECK ([avg_daily_calories]>=(0)),
    CHECK ([avg_daily_steps]>=(0)),
    CHECK ([avg_deep_sleep_hours]>=(0) AND [avg_deep_sleep_hours]<=(12)),
    CHECK ([avg_exercise_minutes]>=(0)),
    CHECK ([avg_heart_rate]>=(30) AND [avg_heart_rate]<=(200)),
    CHECK ([avg_resting_heart_rate]>=(30) AND [avg_resting_heart_rate]<=(100)),
    CHECK ([avg_sleep_hours]>=(0) AND [avg_sleep_hours]<=(24)),
    CHECK ([avg_stress_level]>=(0) AND [avg_stress_level]<=(10)),
    CHECK ([total_active_days]>=(0) AND [total_active_days]<=(31)),
    CHECK ([workout_frequency]>=(0) AND [workout_frequency]<=(7)),
    CONSTRAINT [FK_HealthMetrics_Customers] FOREIGN KEY ([user_id]) REFERENCES [dbo].[Customers] ([user_id]) ON DELETE CASCADE ON UPDATE CASCADE
);


 

